#(1) Converting an Integer into Decimls
import decimal   
#Importing the decimal module:
# The decimal module provides support for fast and precise decimal floating-point arithmetic. It is especially useful for applications requiring exact decimal representation, such as financial calculations.
#                    
integer = 10
#Converting the integer to a Decimal object:

print(decimal.Decimal(integer))
#Printing the type of the Decimal object:

print(type(decimal.Decimal(integer)))






